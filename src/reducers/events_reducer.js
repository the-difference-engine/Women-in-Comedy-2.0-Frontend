import { CREATE_EVENT } from '../actions/types';
import { FETCH_USER_EVENTS } from '../actions';

const INITIAL_STATE =[];

export default function(state = INITIAL_STATE, action) {
  switch(action.type) {
    case CREATE_EVENT:
      return action.payload;
    case FETCH_USER_EVENTS:
			return action.payload.data;
    default:
      return state;
  };
};
